		<p><strong>Une bonne nuit est synonyme d’une bonne journée</strong></p>

		<p>Bien dormir contribue à pouvoir bien fonctionner durant l’éveil. Une bonne nuit de sommeil participe à la récupération tant sur les plans physique que psychique en fonction des activités de la journée. Un sommeil suffisant et surtout satisfaisant soutient le bien-être en journée, en plus de diminuer les risques de certaines maladies et de permettre de maintenir efficacement l’attention durant l’éveil.</p>

		<p>Malheureusement, tout le monde ne bénéficie pas d’un sommeil perçu comme récupérateur. Jusqu’à 30 % de la population présente des plaintes liées au sommeil. Les insomnies peuvent en fait partie. Certains éprouvent des difficultés à s’endormir, d’autres se réveillent souvent pendant la nuit, d’autres encore estiment se réveiller trop tôt ou ne parviennent plus à se rendormir.</p>

		<p>Bien entendu, une mauvaise nuit isolée restera probablement sans conséquences sévères, si au cours des nuits suivantes vous pouvez compenser votre manque et ainsi récupérer. Cependant, si vos insomnies perdurent pendant plusieurs nuits, vous risquer de développer de la fatigue qui peut avoir des conséquences substantielles sur votre forme générale au quotidien. Certains essayent de compenser leurs manques de sommeil par des siestes, d’autres par une douche chaude ou un effort physique intense... Souvent cependant, ces astuces pourront même avoir l’effet inverse.</p>

		<p>Ce qui vous aidera à vous mieux dormir n’aidera pas forcément une autre personne. Tout dépend de votre mode de vie, d’éventuelles mauvaises habitudes et, bien souvent, de rituels d’endormissement bien intentionnés mais pas forcément productifs. N’hésitez donc pas à contacter votre médecin. Il sera le mieux placé pour vous aider à mieux dormir grâce à la connaissance détaillée de vos antécédents.</p>
